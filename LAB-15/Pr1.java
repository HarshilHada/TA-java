public class Pr1 {
    public static void main(String[] args) {
        System.out.println("Min of 5 and 10: " + Math.min(5, 10));
        System.out.println("Max of 5 and 10: " + Math.max(5, 10));
        System.out.println("Random number (0.0 to 1.0): " + Math.random());
        System.out.println("2 raised to the power 3: " + Math.pow(2, 3));
        System.out.println("Square root of 16: " + Math.sqrt(16));
        System.out.println("Round of 5.6: " + Math.round(5.6));
        System.out.println("Ceil of 4.3: " + Math.ceil(4.3));
        System.out.println("Floor of 4.7: " + Math.floor(4.7));
        System.out.println("Absolute of -10: " + Math.abs(-10));
    }
}

