class Pr1{
    public static void main(String[] args) {
        //Using For Loop
        System.out.println("Using For Loop");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        //Using While Loop
        System.out.println("Using While Loop");
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        //Using Do While Loop
        System.out.println("Using Do While Loop");
        int j = 1;
        do {
            System.out.println(j);
            j++;
        } while (j <= 10);
    }
}